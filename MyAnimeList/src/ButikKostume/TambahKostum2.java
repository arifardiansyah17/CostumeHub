    private void BtnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnHapusActionPerformed
         // Mendapatkan baris yang dipilih dari DataPesanan (Tabel)
    int selectedRow = DataPesanan.getSelectedRow();

    // Memastikan baris yang dipilih valid
    if (selectedRow != -1) {
        // Konfirmasi penghapusan data
        int option = JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin menghapus pesanan ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (option == JOptionPane.YES_OPTION) {
            // Menghapus baris yang dipilih dari DataPesanan (Tabel)
            DefaultTableModel dataPesananModel = (DefaultTableModel) DataPesanan.getModel();
            dataPesananModel.removeRow(selectedRow);

            // Menyimpan data ke file
            saveTableData();

            // Menampilkan pesan berhasil
            JOptionPane.showMessageDialog(this, "Pesanan berhasil dihapus", "Sukses", JOptionPane.INFORMATION_MESSAGE);
        }
    } else {
        // Jika tidak ada baris yang dipilih, tampilkan pesan kesalahan
        JOptionPane.showMessageDialog(this, "Pilih salah satu pesanan dari daftar", "Kesalahan", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_BtnHapusActionPerformed

    private void checkOutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkOutActionPerformed
    int totalHarga = 0;
    DefaultTableModel dataPesananModel = (DefaultTableModel) DataPesanan.getModel();

    for (int i = 0; i < dataPesananModel.getRowCount(); i++) {
        int harga = Integer.parseInt(dataPesananModel.getValueAt(i, 1).toString());
        totalHarga += harga;
    }

    harga.setText(Integer.toString(totalHarga));
    }//GEN-LAST:event_checkOutActionPerformed

    
    
    private void populateTableFromFile(String filePath) {
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

    // Hapus semua data yang ada di tabel
    model.setRowCount(0);

    try (BufferedReader reader = new BufferedReader(new FileReader(filePath))) {
        String line;
        while ((line = reader.readLine()) != null) {
            String[] data = line.split(",");
            model.addRow(data);
        }
    } catch (IOException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat membaca file", "Error", JOptionPane.ERROR_MESSAGE);
    }
}
