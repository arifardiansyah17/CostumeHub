    private void tambahPesananActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahPesananActionPerformed
         // Mendapatkan baris yang dipilih dari jTable1
    int selectedRow = jTable1.getSelectedRow();
    
    // Memastikan baris yang dipilih valid
    if (selectedRow != -1) {
        // Mendapatkan data dari baris yang dipilih
        String kostum = jTable1.getValueAt(selectedRow, 0).toString();
        String harga = jTable1.getValueAt(selectedRow, 1).toString();
        // Mengubah format harga menjadi "Rp" tanpa tanda titik
    

        String estimasi = jTable1.getValueAt(selectedRow, 2).toString();
        
        // Menambahkan data ke DataPesanan (Tabel)
        DefaultTableModel dataPesananModel = (DefaultTableModel) DataPesanan.getModel();
        dataPesananModel.addRow(new Object[]{kostum, harga, estimasi});
        
        // Menyimpan data ke file
        saveTableData();
        
        // Menampilkan pesan berhasil
        JOptionPane.showMessageDialog(this, "Pesanan berhasil ditambahkan", "Sukses", JOptionPane.INFORMATION_MESSAGE);
    } else {
        // Jika tidak ada baris yang dipilih, tampilkan pesan kesalahan
        JOptionPane.showMessageDialog(this, "Pilih salah satu kostum dari daftar", "Kesalahan", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_tambahPesananActionPerformed

